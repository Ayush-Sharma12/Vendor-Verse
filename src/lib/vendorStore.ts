const SEED_VENDOR_IDS = new Set([
  'raju-chai-stall',
  'priya-sabziwali',
  'mohan-momos-corner',
  'suresh-paan-snacks',
]);

export function getVendors(): Vendor[] {
  return vendors
    .filter((v) => SEED_VENDOR_IDS.has(v.id) || v.id.startsWith('vendor-'))
    .map((v) => ({ ...v, views: v.views ?? 0 }))
    .sort((a, b) => new Date(b.submission_timestamp).getTime() - new Date(a.submission_timestamp).getTime());
}

export function addVendor(data: Omit<Vendor, 'id' | 'submission_timestamp' | 'status' | 'initials' | 'color'>): Vendor {
  const vendor: Vendor = {
    ...data,
    id: `vendor-${Date.now()}-${Math.random().toString(36).slice(2, 7)}`,
    submission_timestamp: new Date().toISOString(),
    status: 'new',
    initials: getInitials(data.vendor_name),
    color: getColor(data.vendor_name),
    views: 0,
  };
  vendors.unshift(vendor);
  return vendor;
}

export function incrementVendorViews(id: string): Vendor | null {
  const vendor = vendors.find((v) => v.id === id);
  if (!vendor) return null;
  vendor.views = (vendor.views || 0) + 1;
  vendor.last_viewed_timestamp = new Date().toISOString();
  return vendor;
}
